    public UserDashboard(UserDAO user, Login login) {
        this();
        this.user = user;
        this.login = login;
        setLbEmailText();
        LoadDataTransaction();
        LoadDataUserBalance();
        determineUserBalance();
        setUserBlanceText();
        setUserTotalPurchase();
    }

    private void LoadDataTransaction() {
        File file = new File(FILE_NAME_TRANSACTION);
        if (file.length() > 0) {
            try (FileInputStream fis = new FileInputStream(FILE_NAME_TRANSACTION); ObjectInputStream ois = new ObjectInputStream(fis)) {
                transactionList = (List<TransactionDTO>) ois.readObject();  // Đọc danh sách người dùng từ tệp
            } catch (IOException | ClassNotFoundException ex) {
                ex.printStackTrace();
            }
        } else {
            transactionList = new ArrayList<>();  // Nếu tệp rỗng, khởi tạo danh sách trống
        }
    }

    private void LoadDataUserBalance() {
        File file = new File(FILE_NAME_USERBALANCE);
        if (file.length() > 0) {
            try (FileInputStream fis = new FileInputStream(FILE_NAME_USERBALANCE); ObjectInputStream ois = new ObjectInputStream(fis)) {
                userBalanceList = (List<UserBalanceDAO>) ois.readObject();  // Đọc danh sách người dùng từ tệp
            } catch (IOException | ClassNotFoundException ex) {
                ex.printStackTrace();
            }
        } else {
            userBalanceList = new ArrayList<>();  // Nếu tệp rỗng, khởi tạo danh sách trống
        }
    }

    public void setLbEmailText() {
        lbUserEmail.setText(user.getEmail());
    }

    public void setUserBlanceText() {
        txtUserBalance.setText(userBalance.getBalance().toString());
    }

    public void determineUserBalance() {
        if (userBalanceList.size() > 0) {
            String tmpUserID = this.user.getUserID();
            for (UserBalanceDAO userBalanceDAO : userBalanceList) {
                if (userBalanceDAO.getUserID().equals(tmpUserID)) {
                    this.userBalance = userBalanceDAO;
                }
            }
        }
    }

    public void setUserTotalPurchase() {
        Double tmpTotal = (double) 0;
        for (TransactionDTO transactionDTO : transactionList) {
            if (transactionDTO.getUserID().equals(this.user.getUserID())) {
                tmpTotal += transactionDTO.getTotalPrice();
            }
        }
        txtTotalPurchase.setText(tmpTotal.toString());
    }

    public void setAfterPurchaseSuccess(UserBalanceDAO newUserBalanceDAO) {
        this.userBalance = newUserBalanceDAO;
        LoadDataTransaction();
        LoadDataUserBalance();
        setUserBlanceText();
        setUserTotalPurchase();
    }

