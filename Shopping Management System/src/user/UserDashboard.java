    private void btnAddMoreBalanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddMoreBalanceActionPerformed
        // TODO add your handling code here:
        if (txtAddMoreBalance.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(rootPane, "Please add more balance first!", "Warning", 2);
        } else {
            Double tmpAddMoreBalance = Double.parseDouble(txtAddMoreBalance.getText().toString());
            for (int i = 0; i < userBalanceList.size(); i++) {
                UserBalanceDAO tmpUserBalanceDAO = userBalanceList.get(i);
                if (tmpUserBalanceDAO.getUserID().equals(this.user.getUserID())) {
                    tmpUserBalanceDAO.setBalance(tmpUserBalanceDAO.getBalance() + tmpAddMoreBalance);
                    login.setUserBalanceListDataReplace(userBalanceList);
                    JOptionPane.showMessageDialog(rootPane, "Add Balance Succeed!");
                    this.userBalance = tmpUserBalanceDAO;
                    txtAddMoreBalance.setText("");
                    this.setUserBlanceText();
                    break;
                }
            }
        }
    }//GEN-LAST:event_btnAddMoreBalanceActionPerformed

